import { build } from 'esbuild';
import { mkdirSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');
const entryFile = path.join(projectRoot, 'editor-web', 'src', 'runtime.ts');
const outDir = path.join(projectRoot, 'components', 'notes', 'generated');
const outFile = path.join(outDir, 'nativeCollabRuntime.generated.ts');

mkdirSync(outDir, { recursive: true });

const result = await build({
  entryPoints: [entryFile],
  bundle: true,
  format: 'iife',
  platform: 'browser',
  target: 'es2020',
  charset: 'utf8',
  minify: false,
  write: false,
});

const bundled = result.outputFiles[0]?.text ?? '';

writeFileSync(
  outFile,
  [
    '// Auto-generated by scripts/build-native-collab-runtime.mjs',
    `export const NATIVE_COLLAB_RUNTIME = ${JSON.stringify(bundled)};`,
    '',
  ].join('\n'),
  'utf8'
);

console.log(`Generated ${path.relative(projectRoot, outFile)}`);
